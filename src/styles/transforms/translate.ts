// Create translate with custom value
export const createTranslate = (x?: string, y?: string) => {
  if (x && y) {
    return createStyle("transform", `translate(${x}, ${y})`);
  } else if (x) {
    return createStyle("transform", `translateX(${x})`);
  } else if (y) {
    return createStyle("transform", `translateY(${y})`);
  }
  return createStyle("transform", "translate(0)");
};